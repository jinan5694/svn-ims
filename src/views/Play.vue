<template>
  <Page>
    <template slot="toolbar">
      <Search
        v-model="searchKey"
        @change="handleFetch"/>
      <el-input v-model="searchKey"/>
      <el-button @click="handleFetch">fetch</el-button>
    </template>
    <DataTable
      ref="table"
      url="/WorkOrderHeadService/query"
      :params="params"
      :columns="columns"
      :table-config="tableConfig"
      :pagination-config="paginationConfig">
      <template
        slot="expand"
        slot-scope="{row, index}">
        {{ `${row.id} - ${index}` }}
      </template>
      <template
        slot="test"
        slot-scope="{row, index}">
        <el-button type="text">{{ $t('view') }}</el-button>
      </template>
    </DataTable>
  </Page>
</template>
<script>
// import _ from 'lodash'

export default {
  data () {
    return {
      searchKey: ''
    }
  },
  computed: {
    // 建议 params 参数放在计算属性中，可以方便的与其他变量整合
    columns () {
      return [
        {
          type: 'expand'
        },
        {
          type: 'index'
        },
        {
          type: 'selection'
        },
        {
          label: '姓名',
          prop: 'orderNo'
        },
        {
          label: '日期',
          prop: 'date'
        },
        {
          label: '地址',
          prop: 'address'
        },
        {
          label: '测试',
          slotName: 'test',
          align: 'center',
          width: 100
        }
      ]
    },
    params () {
      return [
        {
          where: {
            and: [
              { enableFlag: 'System_EnableFlag_1' },
              { orderNo: { like: this.searchKey } },
              { orderStatus: { ne: 'AfterSales_OrderStatus_WOStatus_999' } }
            ]
          }
        },
        [
          'servedObj',
          'servedOrg.mcInstance',
          'finVirtualItems.product'
        ]
      ]
    },
    tableConfig () {
      return {
        props: {
          'show-summary': true,
          'summary-method': () => {
            return ['合计', null, null, 10, 30, 40, null]
          }
        },
        events: {
          select: this.handleSelect
        }
      }
    },
    paginationConfig () {
      return {
        props: {
          // small: true
        },
        events: {
          'current-change': this.handleCurrentChange,
          'size-change': this.handleSizeChange
        }
      }
    }
  },
  watch: {
    searchKey () {
      console.log('watch', this.searchKey)
    }
  },
  methods: {
    handleSelect (selection, row) {
      console.log('select', selection, row)
    },
    // events
    handleCurrentChange () {
      console.log('outer handleCurrentChange')
    },
    handleSizeChange () {
      console.log('outer handleSizeChange')
    },
    handleFetch () {
      this.$refs.table.fetch()
    }
  }
}
</script>

